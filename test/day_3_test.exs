defmodule Day3Test do
  use ExUnit.Case
  doctest Day3

  describe "following a slope of right 3, down 1" do
    test "one counts seven trees for the grid" do
      grid = [
        "..##.........##.........##.........##.........##.........##.......",
        "#...#...#..#...#...#..#...#...#..#...#...#..#...#...#..#...#...#..",
